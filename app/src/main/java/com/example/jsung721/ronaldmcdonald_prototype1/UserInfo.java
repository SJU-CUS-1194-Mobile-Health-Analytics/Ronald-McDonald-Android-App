public class UserInfo {
    public int distance;
    public int time;

    public UserInfo() {
        distance = 0;
        time = 0;
    }

    public int getDistance() {
        return distance;
    }

    public void setDistance(int distance) {
        this.distance = distance;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }
}
