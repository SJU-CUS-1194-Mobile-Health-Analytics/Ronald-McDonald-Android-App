public class XYClass
{
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public XYClass(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int x;
    public int y;
}
